package com.sourceit.javabasic.sungurov.lesson26;

class Person 
{ 
  // Instance variables 
    private String name; 
    private int age; 
  
  // Constructor 
    public Person (String name1, int age1) 
    { 
        name = name1; 
        age = age1; 
    } 

  // Instance methods 
    public String getName() 
    { 
        return name; 
    } 

    public int getAge() 
    { 
        return age; 
    } 
} 