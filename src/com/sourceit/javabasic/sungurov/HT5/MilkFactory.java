package com.sourceit.javabasic.sungurov.HT5;

public abstract class MilkFactory {

	private String milk;
	private String icecream;
	private String kefir;
	private String cheese;
	
	
	public String getCheese() {
		return cheese;
	}

	public String setCheese(String cheese) {
		return this.cheese = cheese;
	}

	public String getMilk() {
		return milk;
	}

	public String setMilk(String milk) {
		return this.milk = milk;
	}

	public String getIcecream() {
		return icecream;
	}

	public String setIcecream(String icecream) {
		return this.icecream = icecream;
	}

	public String getKefir() {
		return kefir;
	}

	public String setKefir(String kefir) {
		return this.kefir = kefir;
	}
	
	
	
	
	
	
	
}
